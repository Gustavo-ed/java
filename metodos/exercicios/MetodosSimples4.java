public class MetodosSimples4 {
    public void imprimirNumeros() {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
    public static void main(String[] args) {
        MetodosSimples4 numeros = new MetodosSimples4();
        numeros.imprimirNumeros();
    }
}
